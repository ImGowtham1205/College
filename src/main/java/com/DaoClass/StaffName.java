package com.DaoClass;

//Getters & Setters Class For To Set & Fetch Staff Details
public class StaffName {
	private String name;
	private  int id;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
